-- Maydi's: initial schema for menu + reservations
-- Run in Supabase SQL Editor (or supabase db push)

-- Menu (owner-editable later)
create table if not exists public.menu_items (
  id bigint generated by default as identity primary key,
  name text not null,
  description text not null default '',
  details text not null default '',
  price text not null,
  category text not null,
  image_url text not null default '',
  tags text[] not null default '{}',
  sort_order int not null default 0,
  available boolean not null default true,
  created_at timestamptz not null default now()
);

create index if not exists menu_items_category_idx on public.menu_items (category);
create index if not exists menu_items_sort_idx on public.menu_items (sort_order);

-- Table reservations from /reserve form
create table if not exists public.reservations (
  id uuid primary key default gen_random_uuid(),
  name text not null,
  phone text not null,
  email text,
  date date not null,
  time text not null,
  guests text not null,
  notes text,
  status text not null default 'pending',
  created_at timestamptz not null default now()
);

create index if not exists reservations_date_idx on public.reservations (date desc);
create index if not exists reservations_status_idx on public.reservations (status);

-- RLS
alter table public.menu_items enable row level security;
alter table public.reservations enable row level security;

-- Public can read available menu items
drop policy if exists "Public read menu" on public.menu_items;
create policy "Public read menu"
  on public.menu_items
  for select
  using (available = true);

-- Anyone can insert a reservation (form); no public read
drop policy if exists "Public insert reservations" on public.reservations;
create policy "Public insert reservations"
  on public.reservations
  for insert
  with check (true);

